def operacion_and(x, y):
    """Retorna True si ambos valores son verdaderos."""
    return x and y  

def operacion_or(x, y):
    """Retorna True si al menos uno de los valores es verdadero."""
    return x or y  

def operacion_not(x):
    """Retorna el valor opuesto de x."""
    return not x  
